// Represents the different states the game can be in.
public enum GameState {
    // The main menu state.
    MENU,
    // The states when the game is being played.
    PLAYING_LEVEL1,
    PLAYING_LEVEL2,
    PLAYING_LEVEL3,
    // The paused state.
    PAUSED,
    // The game over state.
    GAME_OVER,
    // The help screen state.
    HELP,
    // The win state when the player has won.
    WIN
}
